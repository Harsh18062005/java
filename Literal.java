class Literal{
    public static void main(String args[]){
        int a=0xb1111; 
        float b=0123.222f;
        double  c=0x1.8p3;
        char  c='A';
        String d="Hello"; 
        boolean e=true; 

        System.out.println("Integer Literal: " + a);
        System.out.println("Float Literal: " + b);
       System.out.println("Double Literal: " + c);
        System.out.println("String Literal: " + d);
        System.out.println("Boolean Literal: " + e);
    }

}